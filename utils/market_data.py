def get_pazarna_cena(grad: str, zona: str) -> float:
    """Връща средната пазарна цена €/кв.м за град и зона."""
    return CENI_PRODAJBA.get(grad, {}).get(zona, (0, 0, 0))[2]


def get_sreden_naem(grad: str, zona: str, kvadraturi: float = 65.0) -> float:
    """Изчислява очакван месечен наем на база площ."""
    base = NAEMI_DVUSTAEN.get(grad, {}).get(zona, (0, 0, 0))[2]
    if base == 0:
        return 0.0
    adjusted = base * (kvadraturi / NAEM_BAZA_KVM) ** NAEM_PLOSHTAEN_EXPONENT
    return round(adjusted, 0)


def get_progres_koef(etap: str) -> float:
    """Прогрес коефициент (колко ще нарасне цената до Акт 16)."""
    return PROGRES_KOEFICIENTI.get(etap, 1.0)


def get_meseci_do_akt16(etap: str) -> int:
    """Месеци до получаване на Акт 16 от текущия строителен етап."""
    return MESECI_DO_AKT16.get(etap, 0)


def get_remont_na_kvm(tip: str) -> float:
    """Средна цена за ремонт €/кв.м по тип."""
