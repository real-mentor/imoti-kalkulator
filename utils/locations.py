def get_all_cities() -> List[str]:
    """Списък на всички градове, сортирани по азбучен ред (София — първа)."""
    cities = sorted(LOCATIONS.keys())
    if "София" in cities:
        cities.remove("София")
        cities.insert(0, "София")
    return cities


def get_zones(city: str) -> List[str]:
    """Всички зони/квартали за даден град."""
    if city not in LOCATIONS:
        return ["Център", "Средна зона", "Периферия"]
    return list(LOCATIONS[city]["zones"].keys())


def get_avg_price(city: str, zone: str) -> int:
    """Средна продажна цена €/кв.м за град + квартал (ново строителство = база)."""
    try:
        return LOCATIONS[city]["zones"][zone]["avg_sqm"]
    except KeyError:
        if city in LOCATIONS:
            first_zone = next(iter(LOCATIONS[city]["zones"].values()))
            return first_zone["avg_sqm"]
        return 1200


def get_avg_rent(city: str, zone: str, sqm: float = 65.0) -> int:
    """Средна наемна цена (двустаен) €/мес за град + квартал.
    Скалира пропорционално спрямо площта (базово 65 кв.м).
    """
    try:
        base = LOCATIONS[city]["zones"][zone]["rent_2br"]
    except KeyError:
        base = 500
    scale = 1.0 + (sqm - 65.0) / 65.0 * 0.4
    scale = max(0.5, min(scale, 2.5))
    return int(base * scale)


def get_zone_type(city: str, zone: str) -> str:
    """Тип зона: luxury / premium / mid_high / mid / economy / suburb."""
    try:
        return LOCATIONS[city]["zones"][zone]["type"]
    except KeyError:
        return "mid"


